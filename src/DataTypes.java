public class DataTypes {
    public static void  main(String [] args)
    {
        //Integer types
        int x = 10;
        long y = 2133434324L;
        short z = 100;
        byte u = 127;
        //boolean
        boolean t = true; //false
        //floating point
        float xf = 3.005F;//Single Precision Floating Point. End in F to indicate that it is float
        double yd = 4.454544;//Double Precision Floating Point. Default floating point data type
        //character
        char c = 'a';//used to capture characters. stores ascii values of characters
        //String
        String s = "Hello Good Morning";

        //To print something
        System.out.println("Hello this is Java Beginning");
        //If you want to print a variable along with a message
        System.out.println("This is x : "+x);


    }

}
